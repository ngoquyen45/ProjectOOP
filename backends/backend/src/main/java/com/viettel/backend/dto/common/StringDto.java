package com.viettel.backend.dto.common;

public class StringDto {

    private String content;
    
    public StringDto() {
        super();
    }

    public StringDto(String content) {
        super();
        this.content = content;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
    
}
